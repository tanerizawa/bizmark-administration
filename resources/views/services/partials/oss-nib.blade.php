            <i class="fas fa-briefcase text-xl" style="color: {{ $service['color'] }};"></i>
        </span>
        Apa itu OSS (NIB)?
    </h2>
    <div class="prose prose-lg max-w-none text-gray-700">
        <p class="text-lg leading-relaxed mb-4">
            <strong>OSS (Online Single Submission)</strong> adalah sistem perizinan berusaha terintegrasi secara elektronik yang dikelola oleh pemerintah Indonesia. Melalui OSS, pelaku usaha dapat mengurus berbagai perizinan dalam satu platform terpadu tanpa harus mengunjungi banyak instansi.
        </p>
        <p class="leading-relaxed mb-4">
            <strong>NIB (Nomor Induk Berusaha)</strong> adalah identitas pelaku usaha yang diterbitkan melalui sistem OSS. NIB berfungsi sebagai pengganti TDP (Tanda Daftar Perusahaan), API (Angka Pengenal Importir), dan akses kepabeanan. NIB menjadi syarat wajib untuk memulai dan menjalankan usaha di Indonesia.
        </p>
        <p class="leading-relaxed">
            Dengan <strong>PP No. 5 Tahun 2021</strong> tentang Penyelenggaraan Perizinan Berusaha Berbasis Risiko, semua pelaku usaha wajib memiliki NIB sebelum melakukan kegiatan usaha. OSS menggunakan pendekatan Risk-Based Approach (RBA) yang mengkategorikan risiko usaha menjadi rendah, menengah, dan tinggi.
        </p>
    </div>
</section>

