        <i class="fas fa-info-circle mr-3" style="color: {{ $service['color'] }};"></i>
        Apa itu AMDAL?
    </h2>
    <p class="text-gray-600 leading-relaxed">
        <strong class="text-gray-800">AMDAL (Analisis Mengenai Dampak Lingkungan)</strong> adalah kajian mengenai dampak penting
        dari suatu rencana usaha dan/atau kegiatan terhadap lingkungan hidup yang diperlukan dalam proses
        pengambilan keputusan penyelenggaraan usaha dan/atau kegiatan.
    </p>
    <p class="text-gray-600 leading-relaxed">
        AMDAL berfungsi sebagai instrumen pencegahan pencemaran dan/atau kerusakan lingkungan, sekaligus memastikan
        kegiatan usaha berjalan sesuai kaidah keberlanjutan dan peraturan perundang-undangan yang berlaku.
    </p>
    <div class="mt-6 p-4 bg-white border-2 rounded-xl" style="border-color: {{ $service['color'] }}33;">
