                <i class="fas fa-leaf" style="color: {{ $service['color'] }};"></i>
            </div>
            <p class="text-sm text-gray-700 leading-relaxed">
                Sesuai <strong>PP No. 22 Tahun 2021</strong> tentang Penyelenggaraan Perlindungan dan Pengelolaan Lingkungan Hidup,
                AMDAL wajib untuk kegiatan yang berpotensi menimbulkan dampak besar dan penting terhadap lingkungan. Persetujuan
                atas dokumen AMDAL menjadi bagian dari <em>persetujuan lingkungan</em> yang terintegrasi dalam perizinan berusaha.
