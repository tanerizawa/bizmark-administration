    </h2>
    <div class="prose prose-lg max-w-none text-gray-700">
        <p class="text-lg leading-relaxed mb-4">
            <strong>UKL-UPL (Upaya Pengelolaan Lingkungan Hidup dan Upaya Pemantauan Lingkungan Hidup)</strong> adalah dokumen lingkungan yang wajib dimiliki oleh usaha dan/atau kegiatan yang tidak termasuk dalam kategori wajib AMDAL. UKL-UPL merupakan instrumen pencegahan pencemaran dan/atau kerusakan lingkungan hidup untuk kegiatan yang berdampak tidak penting terhadap lingkungan.
        </p>
        <p class="leading-relaxed mb-4">
