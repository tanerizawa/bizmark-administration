        </p>
        <p class="leading-relaxed">
            Sesuai <strong>PP No. 22 Tahun 2021</strong>, UKL-UPL berlaku untuk usaha skala kecil-menengah seperti industri rumah tangga, retail, perhotelan, perkantoran, dan sektor jasa lainnya yang tidak termasuk dalam daftar wajib AMDAL.
        </p>
