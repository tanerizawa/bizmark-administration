<section class="mb-12" data-aos="fade-up">
    <h2 class="text-3xl font-bold text-gray-900 mb-6 flex items-center">
        <span class="w-12 h-12 rounded-lg flex items-center justify-center mr-4" style="background: linear-gradient(135deg, {{ $service['color'] }}20, {{ $service['color'] }}40);">
            <i class="fas fa-building text-xl" style="color: {{ $service['color'] }};"></i>
        </span>
        Apa itu PBG/SLF?
    </h2>
    <div class="prose prose-lg max-w-none text-gray-700">
        <p class="text-lg leading-relaxed mb-4">
            <strong>PBG (Persetujuan Bangunan Gedung)</strong> adalah perizinan yang diberikan kepada pemilik bangunan gedung untuk membangun baru, mengubah, memperluas, mengurangi, dan/atau merawat bangunan gedung sesuai dengan standar teknis bangunan gedung. PBG menggantikan IMB (Izin Mendirikan Bangunan) dalam sistem OSS.
        </p>
        <p class="leading-relaxed mb-4">
            <strong>SLF (Sertifikat Laik Fungsi)</strong> adalah sertifikat yang diberikan oleh pemerintah daerah untuk menyatakan kelaikan fungsi suatu bangunan gedung, baik secara administratif maupun teknis. SLF wajib dimiliki sebelum bangunan digunakan/beroperasi dan harus diperpanjang setiap 5 tahun untuk bangunan non-hunian atau 20 tahun untuk hunian.
        </p>
        <p class="leading-relaxed">
            Berdasarkan <strong>UU No. 28 Tahun 2002</strong> tentang Bangunan Gedung dan <strong>PP No. 16 Tahun 2021</strong>, setiap bangunan gedung wajib memiliki PBG sebelum konstruksi dimulai dan SLF sebelum bangunan difungsikan. Pelanggaran dapat berakibat sanksi administratif hingga pembongkaran bangunan.
        </p>
    </div>
</section>

